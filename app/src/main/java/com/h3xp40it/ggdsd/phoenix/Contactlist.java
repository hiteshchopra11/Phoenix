package com.h3xp40it.ggdsd.phoenix;

class Contactlist {

    private int img;
    private String name;
    private String number;

    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

    Contactlist(int img, String name, String number) {
        this.img= img;
        this.name = name;
        this.number= number;

    }


}
